import turtle

# Square
kevin_turtle = turtle.Turtle()


def square():
    kevin_turtle.forward(100)
    kevin_turtle.right(90)
    kevin_turtle.forward(100)
    kevin_turtle.right(90)
    kevin_turtle.forward(100)
    kevin_turtle.right(90)
    kevin_turtle.forward(100)


square()
kevin_turtle.forward(100)
square()

turtle.done()


